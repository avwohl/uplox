"""Python backend: emit a self-contained module that reuses the plox runtime.

The emitted file is a thin wrapper — it embeds the canonical JSON bundle
as a literal and exposes a top-level :func:`parse` that drives the
:mod:`plox.parse.runtime` driver. Hosts that ship a plox-generated parser
to a Python app vendor the emitted module and pull in plox as a runtime
dependency. The runtime is small (lexer + LR driver + hooks) and shared
across all grammars, so embedding versus duplicating is a clear win.

The module also exposes the embedded ``BUNDLE`` so hosts can inspect the
schema directly (token names, productions, etc.) without re-parsing.
"""

from __future__ import annotations

import json
import re
from typing import Any


_VALID_PREFIX = re.compile(r"^[a-zA-Z_][a-zA-Z0-9_]*$")


def emit_py(bundle: dict[str, Any], *, prefix: str | None = None) -> str:
    """Emit a Python module text for ``bundle``.

    ``prefix`` overrides the bundle's grammar name in the module's TOKENS
    constant and serves as a sanity check that the value is a legal Python
    identifier — emitted names use it directly.
    """
    grammar = (prefix or bundle["meta"]["grammar"]).strip()
    if not _VALID_PREFIX.match(grammar):
        raise ValueError(
            f"prefix {grammar!r} is not a valid Python identifier; "
            "use --prefix to override"
        )

    bundle_json = json.dumps(bundle, indent=2, sort_keys=True)
    # Triple-quote-safe encoding: backslash any embedded triple quote.
    bundle_json_safe = bundle_json.replace('"""', r'\"""')

    lines: list[str] = [
        f'"""Auto-generated Python parser for grammar {grammar!r}.',
        "",
        "Generated by plox. Depends on the plox runtime at import time;",
        "no other state is shared with the generator.",
        '"""',
        "",
        "from __future__ import annotations",
        "",
        "import json",
        "from typing import Any, Callable, Iterable",
        "",
        "from plox.lex.scanner import Scanner, Token",
        "from plox.parse.runtime import (",
        "    HookRegistry,",
        "    ParseContext,",
        "    ParseError,",
        "    ParseNode,",
        "    SemanticAction,",
        "    StackValue,",
        "    TokenFilter,",
        "    parse as _runtime_parse,",
        ")",
        "from plox.tables import balanced_from_json, dfa_from_json, table_from_json",
        "",
        "",
        "BUNDLE: dict[str, Any] = json.loads(r\"\"\"",
        bundle_json_safe,
        "\"\"\")",
        "",
        "_dfa, _tokens, _skip = dfa_from_json(BUNDLE[\"lex\"])",
        "_table = table_from_json(BUNDLE[\"parse\"])",
        "_scanner = Scanner(",
        "    dfa=_dfa,",
        "    skip_tokens=frozenset(_skip),",
        "    balanced=balanced_from_json(BUNDLE[\"lex\"]),",
        ")",
        "",
        "TOKENS: list[str] = list(_tokens)",
        "SKIP: list[str] = list(_skip)",
        f"GRAMMAR_NAME: str = {grammar!r}",
        "",
        "",
        "def parse(",
        "    text: str,",
        "    *,",
        "    hooks: HookRegistry | None = None,",
        "    semantic_actions: dict[int, SemanticAction] | None = None,",
        "    token_filter: TokenFilter | None = None,",
        ") -> StackValue:",
        f'    """Parse ``text`` against the embedded {grammar} grammar.',
        "",
        "    Returns the value associated with the start symbol — by default a",
        "    :class:`plox.parse.runtime.ParseNode`, but anything a host's",
        "    ``semantic_actions`` returns is fine.",
        "",
        "    Raises :class:`plox.parse.runtime.ParseError` on a syntax error or",
        "    :class:`plox.lex.scanner.ScanError` on a lexical error.",
        '    """',
        "    return _runtime_parse(",
        "        _table,",
        "        _scanner.scan(text),",
        "        hooks=hooks or HookRegistry(ignore_missing=True),",
        "        semantic_actions=semantic_actions,",
        "        token_filter=token_filter,",
        "    )",
        "",
        "",
        "def scan(text: str) -> Iterable[Token]:",
        f'    """Run only the lexer on ``text`` and yield tokens. Useful for"""',
        f'    """tooling that wants the token stream without parsing."""',
        "    return _scanner.scan(text)",
        "",
        "",
        "__all__ = [",
        "    \"BUNDLE\", \"TOKENS\", \"SKIP\", \"GRAMMAR_NAME\",",
        "    \"parse\", \"scan\",",
        "    \"HookRegistry\", \"ParseContext\", \"ParseError\", \"ParseNode\", \"Token\",",
        "]",
        "",
    ]
    return "\n".join(lines)
