"""C++ code emitter. Emits ``(header_text, impl_text)`` from a JSON bundle.

Mirrors the C backend's deliverables (re-entrancy, single namespace) but
exposes a more idiomatic API:

* ``plox::<grammar>::Parser`` — class with a constructor that takes a
  ``std::string_view`` input, a ``parse()`` method returning ``bool``, and
  ``root()`` / ``error()`` accessors.
* ``plox::<grammar>::Node`` — tree node holding kind, position, text view,
  and a vector of ``std::unique_ptr<Node>`` children.
* ``plox::<grammar>::Token`` and ``NonTerminal`` enums.

All escaping identifiers sit inside ``namespace plox::<grammar>``. Tables
are file-static const inside an anonymous namespace in the .cpp so two
grammars built by plox link together without conflicting symbol or
ODR violations.
"""

from __future__ import annotations

import re
from typing import Any

GENERATED_NOTE = (
    "/* Generated by plox. Do not edit by hand. */\n"
    "/* Self-contained C++ front-end: scanner + LR(1) parser. */\n"
)


def emit_cpp(bundle: dict[str, Any], *, prefix: str | None = None) -> tuple[str, str]:
    """Emit ``(header, impl)`` source text for ``bundle``.

    ``prefix`` overrides the grammar-derived namespace name when set.
    The prefix must be a valid C++ identifier fragment.
    """
    grammar_name = (prefix or bundle.get("meta", {}).get("grammar") or "grammar").strip()
    if not re.match(r"^[A-Za-z_][A-Za-z0-9_]*$", grammar_name):
        raise ValueError(f"grammar name {grammar_name!r} is not a valid C++ identifier")

    lex = bundle.get("lex") or {}
    parse = bundle.get("parse") or {}
    if not lex.get("states"):
        raise ValueError("bundle has no lex section; cannot emit C++")
    if not parse.get("states"):
        raise ValueError("bundle has no parse section; cannot emit C++")

    ctx = _CppCtx(grammar=grammar_name, bundle=bundle, lex=lex, parse=parse)
    return _emit_header(ctx), _emit_impl(ctx)


class _CppCtx:
    def __init__(self, *, grammar: str, bundle, lex, parse) -> None:
        self.grammar = grammar
        self.bundle = bundle
        self.lex = lex
        self.parse = parse

        self.tokens: list[str] = list(lex["tokens"])
        self.skip_tokens: set[str] = set(lex.get("skip", []))
        self.non_terminals: list[str] = list(parse["non_terminals"])
        self.productions: list[dict] = list(parse["productions"])
        self.start_state: int = parse.get("start_state", 0)
        self.end_marker: str = parse.get("end_marker", "$")

        self.token_index: dict[str, int] = {self.end_marker: 0}
        for i, name in enumerate(self.tokens, start=1):
            self.token_index[name] = i

        self.nt_index: dict[str, int] = {name: i for i, name in enumerate(self.non_terminals)}


# ---- header -----------------------------------------------------------------


def _emit_header(ctx: _CppCtx) -> str:
    g = ctx.grammar.lower()
    out: list[str] = [GENERATED_NOTE, "#pragma once", "", "#include <cstddef>", "#include <memory>", "#include <string>", "#include <string_view>", "#include <vector>", "", f"namespace plox::{g} {{", ""]

    out.append("/* Token kinds: synthetic end-of-input plus every declared token. */")
    out.append("enum class Token : int {")
    out.append("    _EndOfInput_ = 0,")
    for tok in ctx.tokens:
        out.append(f"    {_token_member(tok)},")
    out.append("    _Count_")
    out.append("};")
    out.append("")

    out.append("/* Non-terminal kinds. */")
    out.append("enum class NonTerminal : int {")
    aug_start = ctx.bundle["parse"].get("augmented_start", "$start")
    for i, nt in enumerate(ctx.non_terminals):
        suffix = "_Start_" if nt == aug_start else _nt_member(nt)
        out.append(f"    {suffix} = {i},")
    out.append(f"    _Count_ = {len(ctx.non_terminals)}")
    out.append("};")
    out.append("")

    out.append("/* Parse-tree node. ``is_terminal`` discriminates terminal vs nonterm.")
    out.append(" * All Node instances are owned by the Parser; children are observer")
    out.append(" * pointers to nodes the Parser owns. The tree is invalidated when the")
    out.append(" * Parser is destroyed. */")
    out.append("struct Node {")
    out.append("    bool is_terminal = false;")
    out.append("    int  kind = 0;            // Token or NonTerminal value")
    out.append("    int  production = -1;     // -1 for terminals")
    out.append("    int  line = 0;")
    out.append("    int  column = 0;")
    out.append("    std::string_view text;    // valid for terminals; into the input buffer")
    out.append("    std::vector<Node*> children;  // observer ptrs; non-owning")
    out.append("};")
    out.append("")

    out.append("class Parser {")
    out.append("public:")
    out.append("    explicit Parser(std::string_view input);")
    out.append("    ~Parser();")
    out.append("")
    out.append("    Parser(const Parser&) = delete;")
    out.append("    Parser& operator=(const Parser&) = delete;")
    out.append("    Parser(Parser&&) = default;")
    out.append("    Parser& operator=(Parser&&) = default;")
    out.append("")
    out.append("    /* Returns true on success. The root tree is owned by the Parser. */")
    out.append("    bool parse();")
    out.append("    const Node* root() const { return root_; }")
    out.append("    const std::string& error() const { return error_; }")
    out.append("")
    out.append("    static const char* token_name(int kind);")
    out.append("    static const char* nt_name(int kind);")
    out.append("")
    out.append("private:")
    out.append("    struct Impl;")
    out.append("    std::unique_ptr<Impl> impl_;")
    out.append("    Node*       root_ = nullptr;")
    out.append("    std::string error_;")
    out.append("};")
    out.append("")
    out.append(f"}}  // namespace plox::{g}")
    return "\n".join(out) + "\n"


# ---- implementation ---------------------------------------------------------


def _emit_impl(ctx: _CppCtx) -> str:
    g = ctx.grammar.lower()
    out: list[str] = [GENERATED_NOTE, f'#include "plox_{g}.hpp"', "", "#include <cstdio>", "#include <cstdlib>", "#include <cstring>", "", f"namespace plox::{g} {{", "namespace {", ""]

    out.extend(_emit_lex_tables(ctx))
    out.append("")
    out.extend(_emit_parse_tables(ctx))
    out.append("")
    out.extend(_emit_name_tables(ctx))
    out.append("")
    out.append("}  // anonymous namespace")
    out.append("")
    out.extend(_emit_impl_struct(ctx))
    out.append("")
    out.extend(_emit_lifecycle_and_lexer(ctx))
    out.append("")
    out.extend(_emit_parser(ctx))
    out.append("")
    out.extend(_emit_name_funcs(ctx))
    out.append("")
    out.append(f"}}  // namespace plox::{g}")
    return "\n".join(out) + "\n"


def _emit_lex_tables(ctx: _CppCtx) -> list[str]:
    states = ctx.lex["states"]
    n = len(states)
    rows = [[-1] * 256 for _ in range(n)]
    accepts = [-1] * n
    for entry in states:
        sid = entry["id"]
        for byte_str, tgt in entry.get("edges", {}).items():
            rows[sid][int(byte_str, 16)] = tgt
        if entry.get("accept"):
            accepts[sid] = ctx.token_index[entry["accept"]]

    skip_bits = [1 if name in ctx.skip_tokens else 0 for name in ctx.tokens]

    out: list[str] = []
    out.append(f"constexpr int kLexStateCount = {n};")
    out.append(f"constexpr int kTokenCount = {len(ctx.tokens) + 1};")
    out.append("")
    out.append("constexpr int kDfaTrans[kLexStateCount][256] = {")
    for s in range(n):
        out.append("    { " + ", ".join(str(v) for v in rows[s]) + " },")
    out.append("};")
    out.append("")
    out.append("constexpr int kDfaAccept[kLexStateCount] = {")
    for s in range(n):
        out.append(f"    {accepts[s]},")
    out.append("};")
    out.append("")
    out.append("constexpr int kTokenSkip[kTokenCount] = {")
    out.append("    0,  // _EndOfInput_")
    for tok, bit in zip(ctx.tokens, skip_bits):
        out.append(f"    {bit},  // {tok}")
    out.append("};")
    return out


def _emit_parse_tables(ctx: _CppCtx) -> list[str]:
    n_states = len(ctx.parse["states"])
    n_tokens = len(ctx.tokens) + 1
    n_nts = len(ctx.non_terminals)
    n_prods = len(ctx.productions)

    action = [[0] * n_tokens for _ in range(n_states)]
    goto = [[-1] * n_nts for _ in range(n_states)]

    for entry in ctx.parse["states"]:
        s = entry["id"]
        for term, code in entry["actions"].items():
            tk = ctx.token_index[term]
            if code == "acc":
                action[s][tk] = -32768
            elif code.startswith("s"):
                action[s][tk] = int(code[1:]) + 1
            elif code.startswith("r"):
                action[s][tk] = -(int(code[1:]) + 1)
            else:
                raise ValueError(f"unrecognised action code {code!r}")
        for nt, tgt in entry["gotos"].items():
            goto[s][ctx.nt_index[nt]] = tgt

    out: list[str] = []
    out.append(f"constexpr int kParseStateCount = {n_states};")
    out.append(f"constexpr int kProductionCount = {n_prods};")
    out.append(f"constexpr int kNtCount = {n_nts};")
    out.append(f"constexpr int kStartState = {ctx.start_state};")
    out.append("constexpr int kActionAccept = -32768;")
    out.append("")
    out.append("constexpr int kAction[kParseStateCount][kTokenCount] = {")
    for s in range(n_states):
        out.append("    { " + ", ".join(str(v) for v in action[s]) + " },")
    out.append("};")
    out.append("")
    out.append("constexpr int kGoto[kParseStateCount][kNtCount] = {")
    for s in range(n_states):
        out.append("    { " + ", ".join(str(v) for v in goto[s]) + " },")
    out.append("};")
    out.append("")

    # Default reduction per state, or -1. Used by the parser when ACTION
    # lookup misses; see plox.parse.lr1.LRTable.default_reductions.
    default_reductions = [-1] * n_states
    for entry in ctx.parse["states"]:
        if "default_reduction" in entry:
            default_reductions[entry["id"]] = entry["default_reduction"]
    out.append("constexpr int kDefaultReduction[kParseStateCount] = {")
    chunk = 16
    for i in range(0, n_states, chunk):
        out.append("    " + ", ".join(str(v) for v in default_reductions[i : i + chunk]) + ",")
    out.append("};")
    out.append("")

    out.append("struct Production { int lhs; int rhs_len; };")
    out.append("constexpr Production kProductions[kProductionCount] = {")
    for p in ctx.productions:
        out.append(f"    {{ {ctx.nt_index[p['lhs']]}, {len(p['rhs'])} }},")
    out.append("};")
    return out


def _emit_name_tables(ctx: _CppCtx) -> list[str]:
    out: list[str] = []
    out.append("constexpr const char* kTokenNames[kTokenCount] = {")
    out.append('    "$",')
    for tok in ctx.tokens:
        out.append(f'    "{_c_string_escape(tok)}",')
    out.append("};")
    out.append("")
    out.append("constexpr const char* kNtNames[kNtCount] = {")
    for nt in ctx.non_terminals:
        out.append(f'    "{_c_string_escape(nt)}",')
    out.append("};")
    return out


def _emit_impl_struct(ctx: _CppCtx) -> list[str]:
    return [
        "struct Parser::Impl {",
        "    std::string_view input;",
        "    int  lex_pos = 0;",
        "    int  lex_line = 1;",
        "    int  lex_column = 1;",
        "",
        "    std::vector<int>                    state_stack;",
        "    std::vector<Node*>                  value_stack;",
        "",
        "    std::vector<std::unique_ptr<Node>>  all_nodes;  // ownership",
        "",
        "    Node* track(std::unique_ptr<Node> n) {",
        "        Node* raw = n.get();",
        "        all_nodes.push_back(std::move(n));",
        "        return raw;",
        "    }",
        "",
        "    /* Maximal-munch DFA scanner. Returns -1 on lex error, else token kind. */",
        "    int next_token(int& out_pos, int& out_len, int& out_line, int& out_col) {",
        "        while (true) {",
        "            if (lex_pos >= static_cast<int>(input.size())) {",
        "                out_pos = lex_pos; out_len = 0;",
        "                out_line = lex_line; out_col = lex_column;",
        "                return 0;  // _EndOfInput_",
        "            }",
        "            int state = 0;",
        "            int last_accept_end = -1;",
        "            int last_accept_kind = -1;",
        "            if (kDfaAccept[0] >= 0) { last_accept_end = lex_pos; last_accept_kind = kDfaAccept[0]; }",
        "            int i = lex_pos;",
        "            while (i < static_cast<int>(input.size())) {",
        "                unsigned char b = static_cast<unsigned char>(input[i]);",
        "                int nxt = kDfaTrans[state][b];",
        "                if (nxt < 0) break;",
        "                state = nxt;",
        "                ++i;",
        "                if (kDfaAccept[state] >= 0) {",
        "                    last_accept_end = i;",
        "                    last_accept_kind = kDfaAccept[state];",
        "                }",
        "            }",
        "            if (last_accept_end <= lex_pos) return -1;",
        "            int start = lex_pos;",
        "            int sline = lex_line, scol = lex_column;",
        "            for (int p = start; p < last_accept_end; ++p) {",
        "                if (input[p] == '\\n') { lex_line++; lex_column = 1; }",
        "                else { lex_column++; }",
        "            }",
        "            lex_pos = last_accept_end;",
        "            if (kTokenSkip[last_accept_kind]) continue;",
        "            out_pos = start;",
        "            out_len = last_accept_end - start;",
        "            out_line = sline;",
        "            out_col = scol;",
        "            return last_accept_kind;",
        "        }",
        "    }",
        "};",
    ]


def _emit_lifecycle_and_lexer(ctx: _CppCtx) -> list[str]:
    return [
        "Parser::Parser(std::string_view input) : impl_(std::make_unique<Impl>()) {",
        "    impl_->input = input;",
        "    impl_->state_stack.reserve(64);",
        "    impl_->value_stack.reserve(64);",
        "}",
        "Parser::~Parser() = default;",
    ]


def _emit_parser(ctx: _CppCtx) -> list[str]:
    return [
        "bool Parser::parse() {",
        "    auto& im = *impl_;",
        "    im.state_stack.clear();",
        "    im.value_stack.clear();",
        "    im.state_stack.push_back(kStartState);",
        "",
        "    int la_pos, la_len, la_line, la_col;",
        "    int la_kind = im.next_token(la_pos, la_len, la_line, la_col);",
        "    if (la_kind < 0) { error_ = \"lexical error\"; return false; }",
        "",
        "    while (true) {",
        "        int s = im.state_stack.back();",
        "        int act = kAction[s][la_kind];",
        "        if (act == 0) {",
        "            int dr = kDefaultReduction[s];",
        "            if (dr >= 0) {",
        "                /* See LRTable.default_reductions. Reducing eagerly",
        "                   on a state with only one valid reduction lets a",
        "                   host-side token rewriter (e.g. C typedef-name)",
        "                   update its tables before the next ACTION lookup. */",
        "                act = -(dr + 1);",
        "            } else {",
        "                char buf[200];",
        "                std::snprintf(buf, sizeof(buf), \"unexpected token %s at line %d, column %d\",",
        "                              token_name(la_kind), la_line, la_col);",
        "                error_ = buf;",
        "                return false;",
        "            }",
        "        }",
        "        if (act == kActionAccept) {",
        "            root_ = im.value_stack.back();",
        "            return true;",
        "        }",
        "        if (act > 0) {",
        "            auto leaf = std::make_unique<Node>();",
        "            leaf->is_terminal = true;",
        "            leaf->kind = la_kind;",
        "            leaf->line = la_line;",
        "            leaf->column = la_col;",
        "            leaf->text = im.input.substr(la_pos, la_len);",
        "            Node* raw = im.track(std::move(leaf));",
        "            im.state_stack.push_back(act - 1);",
        "            im.value_stack.push_back(raw);",
        "            la_kind = im.next_token(la_pos, la_len, la_line, la_col);",
        "            if (la_kind < 0) { error_ = \"lexical error\"; return false; }",
        "            continue;",
        "        }",
        "        int prod = -act - 1;",
        "        Production p = kProductions[prod];",
        "        auto node = std::make_unique<Node>();",
        "        node->is_terminal = false;",
        "        node->kind = p.lhs;",
        "        node->production = prod;",
        "        node->children.reserve(p.rhs_len);",
        "        if (p.rhs_len > 0) {",
        "            int base = static_cast<int>(im.value_stack.size()) - p.rhs_len;",
        "            for (int i = 0; i < p.rhs_len; ++i) {",
        "                node->children.push_back(im.value_stack[base + i]);",
        "            }",
        "            im.state_stack.resize(im.state_stack.size() - p.rhs_len);",
        "            im.value_stack.resize(im.value_stack.size() - p.rhs_len);",
        "            if (!node->children.empty() && node->children.front()) {",
        "                node->line = node->children.front()->line;",
        "                node->column = node->children.front()->column;",
        "            }",
        "        }",
        "        Node* raw = node.get();",
        "        im.all_nodes.push_back(std::move(node));",
        "        int gs = im.state_stack.back();",
        "        int target = kGoto[gs][p.lhs];",
        "        if (target < 0) {",
        "            im.value_stack.push_back(raw);",
        "            continue;",
        "        }",
        "        im.state_stack.push_back(target);",
        "        im.value_stack.push_back(raw);",
        "    }",
        "}",
    ]


def _emit_name_funcs(ctx: _CppCtx) -> list[str]:
    return [
        "const char* Parser::token_name(int kind) {",
        "    if (kind < 0 || kind >= kTokenCount) return \"<invalid>\";",
        "    return kTokenNames[kind];",
        "}",
        "",
        "const char* Parser::nt_name(int kind) {",
        "    if (kind < 0 || kind >= kNtCount) return \"<invalid>\";",
        "    return kNtNames[kind];",
        "}",
    ]


def _token_member(name: str) -> str:
    return name


def _nt_member(name: str) -> str:
    return re.sub(r"[^A-Za-z0-9_]", "_", name)


def _c_string_escape(s: str) -> str:
    return s.replace("\\", "\\\\").replace('"', '\\"')
