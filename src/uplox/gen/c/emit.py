"""C code emitter for uplox bundles.

Takes a parsed bundle dict and a grammar name, returns a ``(header_text,
impl_text)`` pair. The returned strings are complete files — write them
verbatim to ``uplox_<grammar>.h`` and ``uplox_<grammar>.c``.

Layout:

* Header — public types and function declarations only. No table data.
* Implementation — embedded DFA / parse tables as ``static const`` arrays,
  the scanner state machine, the shift-reduce driver.

All identifiers exposed by the header are prefixed with ``uplox_<grammar>_``
(lower case) for functions and types, and ``UPLOX_<GRAMMAR>_`` (upper case)
for enum members and macros. The bundle's ``meta.grammar`` provides the
canonical name; callers can override via the ``prefix`` argument.

The emitter generates code for the canonical LR(1) tables only. Bundles
built without conflicts (the only kind ``uplox build`` produces in v0) are
the input contract.
"""

from __future__ import annotations

import re
from typing import Any

UPLOX_C_RUNTIME_NOTE = (
    "/* Generated by uplox. Do not edit by hand. */\n"
    "/* Self-contained C front-end: scanner + LR(1) parser. */\n"
)


# ---- entry point -------------------------------------------------------------


def emit_c(bundle: dict[str, Any], *, prefix: str | None = None) -> tuple[str, str]:
    """Emit ``(header, impl)`` source text for ``bundle``.

    ``prefix`` overrides the grammar-derived prefix when set; otherwise the
    bundle's ``meta.grammar`` is used. The prefix must be a valid C identifier
    fragment — the emitter raises :class:`ValueError` if it is not.
    """
    grammar_name = (prefix or bundle.get("meta", {}).get("grammar") or "grammar").strip()
    if not re.match(r"^[A-Za-z_][A-Za-z0-9_]*$", grammar_name):
        raise ValueError(f"grammar name {grammar_name!r} is not a valid C identifier")

    lex = bundle.get("lex") or {}
    parse = bundle.get("parse") or {}
    if not lex.get("states"):
        raise ValueError("bundle has no lex section; cannot emit C")
    if not parse.get("states"):
        raise ValueError("bundle has no parse section; cannot emit C")

    ctx = _EmitContext(grammar=grammar_name, bundle=bundle, lex=lex, parse=parse)

    header = _emit_header(ctx)
    impl = _emit_impl(ctx)
    return header, impl


# ---- shared context ----------------------------------------------------------


class _EmitContext:
    def __init__(self, *, grammar: str, bundle, lex, parse) -> None:
        self.grammar = grammar
        self.bundle = bundle
        self.lex = lex
        self.parse = parse

        self.tokens: list[str] = list(lex["tokens"])  # declaration order
        self.skip_tokens: set[str] = set(lex.get("skip", []))
        # %balanced= map: token name -> closing-delimiter character (single
        # char). Tokens not in the map have a 0 entry in the emitted
        # token_balanced array.
        self.balanced: dict[str, str] = dict(lex.get("balanced") or {})
        self.non_terminals: list[str] = list(parse["non_terminals"])  # sorted from JSON
        self.productions: list[dict] = list(parse["productions"])
        self.start_state: int = parse.get("start_state", 0)
        self.end_marker: str = parse.get("end_marker", "$")

        # Build symbol-to-index maps.
        # Token index 0 is the END marker so the C enum starts the same way.
        self.token_index: dict[str, int] = {self.end_marker: 0}
        for i, name in enumerate(self.tokens, start=1):
            self.token_index[name] = i

        self.nt_index: dict[str, int] = {}
        for i, name in enumerate(self.non_terminals):
            self.nt_index[name] = i

    # convenience
    @property
    def lower(self) -> str:
        return self.grammar.lower()

    @property
    def upper(self) -> str:
        return self.grammar.upper()

    def fname(self, suffix: str) -> str:
        return f"uplox_{self.lower}_{suffix}"

    def macro(self, suffix: str) -> str:
        return f"UPLOX_{self.upper}_{suffix}"


# ---- header emission ---------------------------------------------------------


def _emit_header(ctx: _EmitContext) -> str:
    G = ctx.upper
    g = ctx.lower
    guard = f"UPLOX_{G}_H"
    out: list[str] = [UPLOX_C_RUNTIME_NOTE, f"#ifndef {guard}", f"#define {guard}", "", "#include <stddef.h>", "", '#ifdef __cplusplus', 'extern "C" {', '#endif', ""]

    # Token kind enum.
    out.append("/* Token kinds (terminals + synthetic end-marker). */")
    out.append(f"typedef enum {{")
    out.append(f"    {ctx.macro('TOK__EOI_')} = 0,")
    for tok in ctx.tokens:
        out.append(f"    {ctx.macro('TOK_' + tok)},")
    out.append(f"    {ctx.macro('TOK__COUNT__')}")
    out.append(f"}} uplox_{g}_token_kind;")
    out.append("")

    # Non-terminal enum.
    out.append("/* Non-terminal kinds. */")
    out.append(f"typedef enum {{")
    for i, nt in enumerate(ctx.non_terminals):
        sanitised = re.sub(r"[^A-Za-z0-9_]", "_", nt).upper()
        suffix = "NT__START__" if nt == ctx.bundle["parse"].get("augmented_start", "$start") else f"NT_{sanitised}"
        out.append(f"    {ctx.macro(suffix)} = {i},")
    out.append(f"    {ctx.macro('NT__COUNT__')} = {len(ctx.non_terminals)}")
    out.append(f"}} uplox_{g}_nt_kind;")
    out.append("")

    # Node struct.
    out.append("/* Parse-tree node. The same struct represents both terminals and")
    out.append(" * non-terminals; check ``is_terminal`` to discriminate. */")
    out.append(f"typedef struct uplox_{g}_node uplox_{g}_node;")
    out.append(f"struct uplox_{g}_node {{")
    out.append("    int   kind;          /* uplox_<g>_token_kind or uplox_<g>_nt_kind */")
    out.append("    int   is_terminal;   /* nonzero for tokens */")
    out.append("    int   production;    /* -1 for terminals; production index otherwise */")
    out.append("    int   line;")
    out.append("    int   column;")
    out.append("    const char *text;    /* into the input buffer; not NUL-terminated */")
    out.append("    int   text_len;")
    out.append(f"    uplox_{g}_node **children;")
    out.append("    int   num_children;")
    out.append("};")
    out.append("")

    # Forward declaration of the ctx struct.
    out.append(f"typedef struct uplox_{g}_ctx uplox_{g}_ctx;")
    out.append("")

    # Public API.
    out.append("/* --- public API --- */")
    out.append(f"uplox_{g}_ctx *uplox_{g}_create(const char *input, int input_len);")
    out.append(f"void           uplox_{g}_destroy(uplox_{g}_ctx *ctx);")
    out.append(f"int            uplox_{g}_parse(uplox_{g}_ctx *ctx, uplox_{g}_node **out_root);")
    out.append(f"const char    *uplox_{g}_error(const uplox_{g}_ctx *ctx);")
    out.append(f"const char    *uplox_{g}_token_name(int token_kind);")
    out.append(f"const char    *uplox_{g}_nt_name(int nt_kind);")
    out.append("")
    out.append("/* --- token-filter (lexer feedback) --- */")
    out.append("/*")
    out.append(" * Token filter callback: receives the parser context, the lookahead's")
    out.append(" * terminal kind, and a pointer to the matched token text (NOT NUL-")
    out.append(" * terminated; use la_len). Returns the (possibly rewritten) terminal")
    out.append(" * kind. The runtime invokes the filter on every freshly fetched")
    out.append(" * lookahead and again after every reduction (so a host that updates")
    out.append(" * its typedef-name table from a hook on the typedef-declaration")
    out.append(" * production sees the change applied to the pending lookahead before")
    out.append(" * the next ACTION lookup). Set to NULL (the default) to disable.")
    out.append(" *")
    out.append(" * The user_data pointer is passed through unchanged; use it to point")
    out.append(" * at your typedef table or whatever per-parse host state the filter")
    out.append(" * needs. uplox does not own this pointer.")
    out.append(" */")
    out.append(f"typedef int (*uplox_{g}_token_filter_fn)(uplox_{g}_ctx *ctx, int la_kind, const char *la_text, int la_len, void *user_data);")
    out.append(f"void uplox_{g}_set_token_filter(uplox_{g}_ctx *ctx, uplox_{g}_token_filter_fn fn, void *user_data);")
    out.append("")
    out.append("/* --- post-reduce hook --- */")
    out.append("/*")
    out.append(" * Post-reduce callback: fires after every successful reduction with the")
    out.append(" * production's index and the just-built parse node. Hosts use it to")
    out.append(" * accumulate state across the parse — typically a typedef-name set,")
    out.append(" * but the mechanism is general. Pair with set_token_filter to close")
    out.append(" * the loop: the hook records names, the filter consults them.")
    out.append(" *")
    out.append(" * The callback fires *before* the runtime re-applies the token filter")
    out.append(" * to the pending lookahead, so any state change is visible by the next")
    out.append(" * action lookup.")
    out.append(" */")
    out.append(f"typedef void (*uplox_{g}_post_reduce_fn)(uplox_{g}_ctx *ctx, int prod_index, uplox_{g}_node *node, void *user_data);")
    out.append(f"void uplox_{g}_set_post_reduce(uplox_{g}_ctx *ctx, uplox_{g}_post_reduce_fn fn, void *user_data);")
    out.append("")

    out.append('#ifdef __cplusplus')
    out.append('}')
    out.append('#endif')
    out.append("")
    out.append(f"#endif /* {guard} */")
    return "\n".join(out) + "\n"


# ---- implementation emission -------------------------------------------------


def _emit_impl(ctx: _EmitContext) -> str:
    g = ctx.lower
    out: list[str] = []
    out.append(UPLOX_C_RUNTIME_NOTE)
    out.append(f'#include "uplox_{g}.h"')
    out.append("#include <stddef.h>")
    out.append("#include <stdlib.h>")
    out.append("#include <string.h>")
    out.append("#include <stdio.h>")
    out.append("")
    out.append('/* ====================================================================== */')
    out.append('/* Static tables                                                          */')
    out.append('/* ====================================================================== */')
    out.append("")

    out.extend(_emit_lex_tables(ctx))
    out.append("")
    out.extend(_emit_parse_tables(ctx))
    out.append("")
    out.extend(_emit_name_tables(ctx))
    out.append("")
    out.extend(_emit_ctx_struct(ctx))
    out.append("")
    out.extend(_emit_lifecycle(ctx))
    out.append("")
    out.extend(_emit_lexer(ctx))
    out.append("")
    out.extend(_emit_parser(ctx))
    out.append("")
    out.extend(_emit_name_funcs(ctx))
    return "\n".join(out) + "\n"


# ---- table emission ---------------------------------------------------------


def _emit_lex_tables(ctx: _EmitContext) -> list[str]:
    g = ctx.lower
    states = ctx.lex["states"]
    n_states = len(states)
    out: list[str] = []
    out.append(f"#define UPLOX_{ctx.upper}_LEX_STATE_COUNT {n_states}")
    out.append("")

    # Build the dense 256-byte transition rows.
    rows: list[list[int]] = [[-1] * 256 for _ in range(n_states)]
    accepts: list[int] = [-1] * n_states
    for entry in states:
        sid = entry["id"]
        for byte_str, tgt in entry.get("edges", {}).items():
            b = int(byte_str, 16)
            rows[sid][b] = tgt
        if entry.get("accept"):
            accepts[sid] = ctx.token_index[entry["accept"]]

    # Skip-token bitmap.
    skip_bits = [1 if name in ctx.skip_tokens else 0 for name in ctx.tokens]

    out.append(f"static const int uplox_{g}_dfa_trans[UPLOX_{ctx.upper}_LEX_STATE_COUNT][256] = {{")
    for s in range(n_states):
        row = rows[s]
        cells = ", ".join(str(v) for v in row)
        out.append(f"    {{ {cells} }},")
    out.append("};")
    out.append("")
    out.append(f"static const int uplox_{g}_dfa_accept[UPLOX_{ctx.upper}_LEX_STATE_COUNT] = {{")
    for s in range(n_states):
        out.append(f"    {accepts[s]},")
    out.append("};")
    out.append("")
    out.append(f"static const int uplox_{g}_token_skip[UPLOX_{ctx.upper}_TOK__COUNT__] = {{")
    out.append("    0, /* TOK__EOI_ */")
    for tok, bit in zip(ctx.tokens, skip_bits):
        out.append(f"    {bit}, /* TOK_{tok} */")
    out.append("};")
    out.append("")
    # Per-token close-delimiter byte for %balanced= tokens. 0 means the token
    # is not balanced. The opening byte is whatever the DFA matched, read
    # back from the input at scan time.
    out.append(f"static const int uplox_{g}_token_balanced[UPLOX_{ctx.upper}_TOK__COUNT__] = {{")
    out.append("    0, /* TOK__EOI_ */")
    for tok in ctx.tokens:
        close_char = ctx.balanced.get(tok)
        if close_char is None:
            out.append(f"    0, /* TOK_{tok} */")
        else:
            out.append(f"    {ord(close_char)}, /* TOK_{tok} */")
    out.append("};")
    return out


def _emit_parse_tables(ctx: _EmitContext) -> list[str]:
    """Emit ACTION and GOTO as 2-D tables.

    Layout:
      action[state][token_kind] -> packed int:
        0          = error / no action
        +N         = shift to state N        (N >= 1; offset by encoding below)
        -K         = reduce by production K  (K = production index; encoded as -(K+1))
        special    = ACCEPT (encoded as INT_MIN-equivalent: -32768)

    GOTO[state][nt_kind] = next_state, or -1 for "no transition".
    """
    g = ctx.lower
    out: list[str] = []
    n_states = len(ctx.parse["states"])
    n_tokens = len(ctx.tokens) + 1  # +1 for END
    n_nts = len(ctx.non_terminals)
    n_prods = len(ctx.productions)

    out.append(f"#define UPLOX_{ctx.upper}_PARSE_STATE_COUNT {n_states}")
    out.append(f"#define UPLOX_{ctx.upper}_PRODUCTION_COUNT {n_prods}")
    out.append(f"#define UPLOX_{ctx.upper}_ACTION_ERROR     0")
    out.append(f"#define UPLOX_{ctx.upper}_ACTION_ACCEPT   (-32768)")
    out.append("")
    out.append("/* Encoding: shift -> +(state+1); reduce -> -(production+1); accept -> -32768; */")
    out.append("")

    action: list[list[int]] = [[0] * n_tokens for _ in range(n_states)]
    goto: list[list[int]] = [[-1] * n_nts for _ in range(n_states)]

    for entry in ctx.parse["states"]:
        s = entry["id"]
        for term, code in entry["actions"].items():
            tk_idx = ctx.token_index[term]
            if code == "acc":
                action[s][tk_idx] = -32768
            elif code.startswith("s"):
                action[s][tk_idx] = int(code[1:]) + 1
            elif code.startswith("r"):
                action[s][tk_idx] = -(int(code[1:]) + 1)
            else:
                raise ValueError(f"unrecognised action code {code!r}")
        for nt, tgt in entry["gotos"].items():
            goto[s][ctx.nt_index[nt]] = tgt

    out.append(f"static const int uplox_{g}_action[UPLOX_{ctx.upper}_PARSE_STATE_COUNT][UPLOX_{ctx.upper}_TOK__COUNT__] = {{")
    for s in range(n_states):
        row = ", ".join(str(v) for v in action[s])
        out.append(f"    {{ {row} }},")
    out.append("};")
    out.append("")
    out.append(f"static const int uplox_{g}_goto[UPLOX_{ctx.upper}_PARSE_STATE_COUNT][UPLOX_{ctx.upper}_NT__COUNT__] = {{")
    for s in range(n_states):
        row = ", ".join(str(v) for v in goto[s])
        out.append(f"    {{ {row} }},")
    out.append("};")
    out.append("")

    # Default-reduction table: -1 for "no default", otherwise the production
    # index to reduce by when ACTION lookup yields 0. Required for grammars
    # whose host updates a name table between tokens (typedef-name etc.) —
    # see uplox.parse.lr1.LRTable.default_reductions.
    default_reductions = [-1] * n_states
    for entry in ctx.parse["states"]:
        if "default_reduction" in entry:
            default_reductions[entry["id"]] = entry["default_reduction"]
    out.append(f"static const int uplox_{g}_default_reduction[UPLOX_{ctx.upper}_PARSE_STATE_COUNT] = {{")
    chunk = 16
    for i in range(0, n_states, chunk):
        row = ", ".join(str(v) for v in default_reductions[i : i + chunk])
        out.append(f"    {row},")
    out.append("};")
    out.append("")

    # Production metadata.
    out.append(f"typedef struct {{")
    out.append("    int lhs;     /* nt_kind */")
    out.append("    int rhs_len;")
    out.append(f"}} uplox_{g}_prod;")
    out.append("")
    out.append(f"static const uplox_{g}_prod uplox_{g}_productions[UPLOX_{ctx.upper}_PRODUCTION_COUNT] = {{")
    for p in ctx.productions:
        out.append(f"    {{ {ctx.nt_index[p['lhs']]}, {len(p['rhs'])} }},")
    out.append("};")
    out.append("")
    out.append(f"#define UPLOX_{ctx.upper}_START_STATE {ctx.start_state}")
    return out


def _emit_name_tables(ctx: _EmitContext) -> list[str]:
    g = ctx.lower
    out: list[str] = []
    out.append(f"static const char *const uplox_{g}_token_names[UPLOX_{ctx.upper}_TOK__COUNT__] = {{")
    out.append('    "$",')
    for tok in ctx.tokens:
        out.append(f'    "{_c_string_escape(tok)}",')
    out.append("};")
    out.append("")
    out.append(f"static const char *const uplox_{g}_nt_names[UPLOX_{ctx.upper}_NT__COUNT__] = {{")
    for nt in ctx.non_terminals:
        out.append(f'    "{_c_string_escape(nt)}",')
    out.append("};")
    return out


# ---- ctx + lifecycle --------------------------------------------------------


def _emit_ctx_struct(ctx: _EmitContext) -> list[str]:
    g = ctx.lower
    return [
        f"struct uplox_{g}_ctx {{",
        "    /* Input */",
        "    const char *input;",
        "    int         input_len;",
        "",
        "    /* Lexer state */",
        "    int lex_pos;",
        "    int lex_line;",
        "    int lex_column;",
        "",
        "    /* Parser stacks (heap-allocated, grow as needed). */",
        "    int   *state_stack;",
        f"    uplox_{g}_node **value_stack;",
        "    int    stack_top;     /* = number of items pushed */",
        "    int    stack_cap;",
        "",
        "    /* Result */",
        f"    uplox_{g}_node *root;",
        "",
        "    /* Error message — stable storage owned by the ctx. */",
        "    char error_buf[256];",
        "    int  has_error;",
        "",
        "    /* Allocated nodes; freed by destroy. */",
        f"    uplox_{g}_node **all_nodes;",
        "    int    num_nodes;",
        "    int    cap_nodes;",
        "",
        "    /* Optional token filter (set via uplox_<g>_set_token_filter). */",
        f"    int  (*token_filter)(uplox_{g}_ctx *ctx, int la_kind, const char *la_text, int la_len, void *user_data);",
        "    void  *token_filter_data;",
        "",
        "    /* Optional post-reduce callback (set via uplox_<g>_set_post_reduce). */",
        f"    void (*post_reduce)(uplox_{g}_ctx *ctx, int prod_index, uplox_{g}_node *node, void *user_data);",
        "    void  *post_reduce_data;",
        "};",
    ]


def _emit_lifecycle(ctx: _EmitContext) -> list[str]:
    g = ctx.lower
    return [
        f"static int uplox_{g}__ensure_stack(uplox_{g}_ctx *c, int needed) {{",
        "    if (c->stack_cap >= needed) return 0;",
        "    int new_cap = c->stack_cap ? c->stack_cap * 2 : 64;",
        "    while (new_cap < needed) new_cap *= 2;",
        "    int *ns = (int *)realloc(c->state_stack, sizeof(int) * (size_t)new_cap);",
        f"    uplox_{g}_node **nv = (uplox_{g}_node **)realloc(c->value_stack, sizeof(uplox_{g}_node *) * (size_t)new_cap);",
        "    if (!ns || !nv) return -1;",
        "    c->state_stack = ns;",
        "    c->value_stack = nv;",
        "    c->stack_cap = new_cap;",
        "    return 0;",
        "}",
        "",
        f"static int uplox_{g}__track(uplox_{g}_ctx *c, uplox_{g}_node *n) {{",
        "    if (c->num_nodes >= c->cap_nodes) {",
        "        int new_cap = c->cap_nodes ? c->cap_nodes * 2 : 64;",
        f"        uplox_{g}_node **na = (uplox_{g}_node **)realloc(c->all_nodes, sizeof(uplox_{g}_node *) * (size_t)new_cap);",
        "        if (!na) return -1;",
        "        c->all_nodes = na;",
        "        c->cap_nodes = new_cap;",
        "    }",
        "    c->all_nodes[c->num_nodes++] = n;",
        "    return 0;",
        "}",
        "",
        f"static uplox_{g}_node *uplox_{g}__node_new(uplox_{g}_ctx *c) {{",
        f"    uplox_{g}_node *n = (uplox_{g}_node *)calloc(1, sizeof(uplox_{g}_node));",
        "    if (!n) return NULL;",
        "    n->production = -1;",
        f"    if (uplox_{g}__track(c, n) != 0) {{ free(n); return NULL; }}",
        "    return n;",
        "}",
        "",
        f"uplox_{g}_ctx *uplox_{g}_create(const char *input, int input_len) {{",
        f"    uplox_{g}_ctx *c = (uplox_{g}_ctx *)calloc(1, sizeof(uplox_{g}_ctx));",
        "    if (!c) return NULL;",
        "    c->input = input;",
        "    c->input_len = input_len;",
        "    c->lex_pos = 0;",
        "    c->lex_line = 1;",
        "    c->lex_column = 1;",
        "    return c;",
        "}",
        "",
        f"void uplox_{g}_destroy(uplox_{g}_ctx *c) {{",
        "    if (!c) return;",
        "    for (int i = 0; i < c->num_nodes; ++i) {",
        "        free(c->all_nodes[i]->children);",
        "        free(c->all_nodes[i]);",
        "    }",
        "    free(c->all_nodes);",
        "    free(c->state_stack);",
        "    free(c->value_stack);",
        "    free(c);",
        "}",
        "",
        f"const char *uplox_{g}_error(const uplox_{g}_ctx *c) {{",
        "    return (c && c->has_error) ? c->error_buf : NULL;",
        "}",
        "",
        f"static void uplox_{g}__set_error(uplox_{g}_ctx *c, const char *msg) {{",
        "    if (c->has_error) return;",
        "    c->has_error = 1;",
        "    snprintf(c->error_buf, sizeof(c->error_buf), \"%s\", msg ? msg : \"parse error\");",
        "}",
        "",
        f"void uplox_{g}_set_token_filter(uplox_{g}_ctx *c, uplox_{g}_token_filter_fn fn, void *user_data) {{",
        "    if (!c) return;",
        "    c->token_filter = fn;",
        "    c->token_filter_data = user_data;",
        "}",
        "",
        f"void uplox_{g}_set_post_reduce(uplox_{g}_ctx *c, uplox_{g}_post_reduce_fn fn, void *user_data) {{",
        "    if (!c) return;",
        "    c->post_reduce = fn;",
        "    c->post_reduce_data = user_data;",
        "}",
    ]


# ---- lexer emission ---------------------------------------------------------


def _emit_lexer(ctx: _EmitContext) -> list[str]:
    g = ctx.lower
    return [
        "/* Maximal-munch DFA lexer. Updates lex_pos/line/column on success. */",
        f"static int uplox_{g}__next_token(uplox_{g}_ctx *c, int *out_kind, int *out_pos, int *out_len, int *out_line, int *out_col) {{",
        "    while (1) {",
        f"        if (c->lex_pos >= c->input_len) {{ *out_kind = {ctx.macro('TOK__EOI_')}; *out_pos = c->lex_pos; *out_len = 0; *out_line = c->lex_line; *out_col = c->lex_column; return 0; }}",
        "",
        f"        int state = 0;",
        "        int last_accept_end = -1;",
        "        int last_accept_kind = -1;",
        "        int i = c->lex_pos;",
        f"        if (uplox_{g}_dfa_accept[0] >= 0) {{ last_accept_end = i; last_accept_kind = uplox_{g}_dfa_accept[0]; }}",
        "        while (i < c->input_len) {",
        "            unsigned char b = (unsigned char)c->input[i];",
        f"            int nxt = uplox_{g}_dfa_trans[state][b];",
        "            if (nxt < 0) break;",
        "            state = nxt;",
        "            ++i;",
        f"            if (uplox_{g}_dfa_accept[state] >= 0) {{",
        f"                last_accept_end = i;",
        f"                last_accept_kind = uplox_{g}_dfa_accept[state];",
        f"            }}",
        "        }",
        "        if (last_accept_end <= c->lex_pos) {",
        f"            uplox_{g}__set_error(c, \"lexical error\");",
        "            return -1;",
        "        }",
        "        /* %balanced= extension: if the matched token has a non-zero",
        "         * close byte, extend the match by counting nested open/close",
        "         * pairs until depth returns to zero. The open byte is whatever",
        "         * the DFA already consumed at c->lex_pos. */",
        f"        int close_b = uplox_{g}_token_balanced[last_accept_kind];",
        "        if (close_b) {",
        "            int open_b = (unsigned char)c->input[c->lex_pos];",
        "            int depth = 1;",
        "            int j = last_accept_end;",
        "            while (j < c->input_len && depth > 0) {",
        "                unsigned char b = (unsigned char)c->input[j];",
        "                if (b == open_b) ++depth;",
        "                else if (b == close_b) --depth;",
        "                ++j;",
        "            }",
        "            if (depth != 0) {",
        f"                uplox_{g}__set_error(c, \"unterminated balanced token\");",
        "                return -1;",
        "            }",
        "            last_accept_end = j;",
        "        }",
        "        int start = c->lex_pos;",
        "        int start_line = c->lex_line;",
        "        int start_col = c->lex_column;",
        "        for (int p = start; p < last_accept_end; ++p) {",
        "            if (c->input[p] == '\\n') { c->lex_line++; c->lex_column = 1; }",
        "            else { c->lex_column++; }",
        "        }",
        "        c->lex_pos = last_accept_end;",
        f"        if (uplox_{g}_token_skip[last_accept_kind]) continue; /* %skip token */",
        "        *out_kind = last_accept_kind;",
        "        *out_pos = start;",
        "        *out_len = last_accept_end - start;",
        "        *out_line = start_line;",
        "        *out_col = start_col;",
        "        return 0;",
        "    }",
        "}",
    ]


# ---- parser emission --------------------------------------------------------


def _emit_parser(ctx: _EmitContext) -> list[str]:
    g = ctx.lower
    return [
        f"int uplox_{g}_parse(uplox_{g}_ctx *c, uplox_{g}_node **out_root) {{",
        f"    if (uplox_{g}__ensure_stack(c, 16) != 0) {{ uplox_{g}__set_error(c, \"out of memory\"); return -1; }}",
        f"    c->state_stack[0] = UPLOX_{ctx.upper}_START_STATE;",
        "    c->stack_top = 1;",
        "",
        "    int la_kind, la_pos, la_len, la_line, la_col;",
        f"    if (uplox_{g}__next_token(c, &la_kind, &la_pos, &la_len, &la_line, &la_col) != 0) return -1;",
        "    if (c->token_filter) la_kind = c->token_filter(c, la_kind, c->input + la_pos, la_len, c->token_filter_data);",
        "",
        "    while (1) {",
        "        int s = c->state_stack[c->stack_top - 1];",
        f"        int act = uplox_{g}_action[s][la_kind];",
        "        if (act == 0) {",
        f"            int dr = uplox_{g}_default_reduction[s];",
        "            if (dr >= 0) {",
        "                /* Default reduction: the only valid actions in this",
        "                   state all reduce by the same production. Reducing",
        "                   eagerly lets a host-installed token rewriter (e.g.",
        "                   the C typedef-name hack) update its tables before",
        "                   the next ACTION lookup classifies the lookahead. */",
        "                act = -(dr + 1);",
        "            } else {",
        "                /* Build 'unexpected ... ; expected one of: ...' by walking the",
        "                 * action row at runtime. UPLOX_<G>_TOK__EOI_ is rendered as",
        "                 * '<end of input>' to match the Python runtime's wording. */",
        "                char buf[1024];",
        "                int len = 0;",
        f"                if (la_kind == {ctx.macro('TOK__EOI_')}) {{",
        "                    len += snprintf(buf + len, sizeof(buf) - len, \"unexpected end of input\");",
        "                } else {",
        f"                    len += snprintf(buf + len, sizeof(buf) - len, \"unexpected token %s at line %d, column %d\", uplox_{g}_token_name(la_kind), la_line, la_col);",
        "                }",
        "                int shown = 0, total = 0;",
        f"                for (int t = 0; t < {ctx.macro('TOK__COUNT__')}; ++t) {{",
        f"                    if (uplox_{g}_action[s][t] == 0) continue;",
        "                    ++total;",
        "                    if (shown >= 12) continue;",
        "                    const char *sep = (shown == 0) ? \"; expected one of: \" : \", \";",
        f"                    const char *name = (t == {ctx.macro('TOK__EOI_')}) ? \"<end of input>\" : uplox_{g}_token_name(t);",
        "                    int n = snprintf(buf + len, sizeof(buf) - len, \"%s%s\", sep, name);",
        "                    if (n < 0 || n >= (int)(sizeof(buf) - len)) break;",
        "                    len += n;",
        "                    ++shown;",
        "                }",
        "                if (total > shown) {",
        "                    snprintf(buf + len, sizeof(buf) - len, \", ... +%d more\", total - shown);",
        "                }",
        f"                uplox_{g}__set_error(c, buf);",
        "                return -1;",
        "            }",
        "        }",
        "        if (act == -32768) {",
        "            /* ACCEPT: top of value stack is the start symbol value. */",
        "            c->root = c->value_stack[c->stack_top - 1];",
        "            *out_root = c->root;",
        "            return 0;",
        "        }",
        "        if (act > 0) {",
        "            /* SHIFT */",
        f"            if (uplox_{g}__ensure_stack(c, c->stack_top + 1) != 0) {{ uplox_{g}__set_error(c, \"out of memory\"); return -1; }}",
        f"            uplox_{g}_node *leaf = uplox_{g}__node_new(c);",
        f"            if (!leaf) {{ uplox_{g}__set_error(c, \"out of memory\"); return -1; }}",
        "            leaf->is_terminal = 1;",
        "            leaf->kind = la_kind;",
        "            leaf->line = la_line;",
        "            leaf->column = la_col;",
        "            leaf->text = c->input + la_pos;",
        "            leaf->text_len = la_len;",
        "            c->state_stack[c->stack_top] = act - 1;",
        "            c->value_stack[c->stack_top] = leaf;",
        "            c->stack_top++;",
        f"            if (uplox_{g}__next_token(c, &la_kind, &la_pos, &la_len, &la_line, &la_col) != 0) return -1;",
        "            if (c->token_filter) la_kind = c->token_filter(c, la_kind, c->input + la_pos, la_len, c->token_filter_data);",
        "            continue;",
        "        }",
        "        /* act < 0 and != -32768 -> REDUCE by production -(act + 1). */",
        "        int prod = -act - 1;",
        f"        uplox_{g}_prod p = uplox_{g}_productions[prod];",
        "        int rhs_len = p.rhs_len;",
        f"        uplox_{g}_node *node = uplox_{g}__node_new(c);",
        f"        if (!node) {{ uplox_{g}__set_error(c, \"out of memory\"); return -1; }}",
        "        node->is_terminal = 0;",
        "        node->kind = p.lhs;",
        "        node->production = prod;",
        "        node->num_children = rhs_len;",
        "        if (rhs_len > 0) {",
        f"            node->children = (uplox_{g}_node **)malloc(sizeof(uplox_{g}_node *) * (size_t)rhs_len);",
        f"            if (!node->children) {{ uplox_{g}__set_error(c, \"out of memory\"); return -1; }}",
        "            for (int i = 0; i < rhs_len; ++i) {",
        "                node->children[i] = c->value_stack[c->stack_top - rhs_len + i];",
        "            }",
        "            int top_token = c->stack_top - rhs_len;",
        "            if (top_token >= 0 && node->children[0]) {",
        "                node->line = node->children[0]->line;",
        "                node->column = node->children[0]->column;",
        "            }",
        "            c->stack_top -= rhs_len;",
        "        }",
        "        int gs = c->state_stack[c->stack_top - 1];",
        f"        int target = uplox_{g}_goto[gs][p.lhs];",
        "        if (target < 0) {",
        "            /* augmented reduce -- next ACTION yields ACCEPT */",
        "            c->value_stack[c->stack_top] = node;",
        "            c->stack_top++;",
        "            continue;",
        "        }",
        f"        if (uplox_{g}__ensure_stack(c, c->stack_top + 1) != 0) {{ uplox_{g}__set_error(c, \"out of memory\"); return -1; }}",
        "        c->state_stack[c->stack_top] = target;",
        "        c->value_stack[c->stack_top] = node;",
        "        c->stack_top++;",
        "        /* Fire the post-reduce hook (if any) BEFORE re-running the",
        "           token filter, so a hook that updates host state (e.g. a",
        "           typedef-name set) is visible to the filter on its next call. */",
        "        if (c->post_reduce) c->post_reduce(c, prod, node, c->post_reduce_data);",
        "        /* Re-apply the filter so a host hook on the just-reduced",
        "           production has a chance to update its tables before the",
        "           next ACTION lookup classifies the still-pending lookahead. */",
        "        if (c->token_filter) la_kind = c->token_filter(c, la_kind, c->input + la_pos, la_len, c->token_filter_data);",
        "    }",
        "}",
    ]


def _emit_name_funcs(ctx: _EmitContext) -> list[str]:
    g = ctx.lower
    return [
        f"const char *uplox_{g}_token_name(int k) {{",
        f"    if (k < 0 || k >= {ctx.macro('TOK__COUNT__')}) return \"<invalid>\";",
        f"    return uplox_{g}_token_names[k];",
        "}",
        "",
        f"const char *uplox_{g}_nt_name(int k) {{",
        f"    if (k < 0 || k >= {ctx.macro('NT__COUNT__')}) return \"<invalid>\";",
        f"    return uplox_{g}_nt_names[k];",
        "}",
    ]


