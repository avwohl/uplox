"""Lua emitter. Produces a single ``.lua`` module per grammar.

The module exports ``M.new(input) -> parser_instance`` and constants
``M.TOK`` / ``M.NT`` for token and non-terminal kinds. Each parser instance
is a fresh table; no module-level mutable state, so two grammars loaded
into the same Lua state (or two instances of the same grammar) run
independently.

Targets Lua 5.3+ — uses integer ``//`` semantics and the integer-typed
``string.byte``. We do *not* require LuaJIT or Lua 5.4 features.

Indexing
--------

Lua tables are 1-indexed. The emitter offsets all DFA-state indices and
byte values by 1 so generated array accesses look natural at the call site
(``trans[state][b + 1]``). Internally we still call them "state 0..N-1"
and "byte 0..255" — the +1 is purely a wire-format detail of the table.
"""

from __future__ import annotations

import re
from typing import Any

from . import ast_emit

GENERATED_NOTE = (
    "-- Generated by uplox. Do not edit by hand.\n"
    "-- Self-contained Lua front-end: scanner + LR(1) parser.\n"
)


def emit_lua(bundle: dict[str, Any], *, prefix: str | None = None) -> str:
    """Emit Lua source text for ``bundle``.

    ``prefix`` overrides the grammar-derived module name when set. Must be
    a valid Lua identifier (the emitter raises :class:`ValueError` otherwise).
    """
    grammar_name = (prefix or bundle.get("meta", {}).get("grammar") or "grammar").strip()
    if not re.match(r"^[A-Za-z_][A-Za-z0-9_]*$", grammar_name):
        raise ValueError(f"grammar name {grammar_name!r} is not a valid Lua identifier")

    lex = bundle.get("lex") or {}
    parse = bundle.get("parse") or {}
    if not lex.get("states"):
        raise ValueError("bundle has no lex section; cannot emit Lua")
    if not parse.get("states"):
        raise ValueError("bundle has no parse section; cannot emit Lua")

    ctx = _LuaCtx(grammar=grammar_name, bundle=bundle, lex=lex, parse=parse)
    return _emit_module(ctx)


class _LuaCtx:
    def __init__(self, *, grammar: str, bundle, lex, parse) -> None:
        self.grammar = grammar
        self.bundle = bundle
        self.lex = lex
        self.parse = parse

        self.tokens: list[str] = list(lex["tokens"])
        self.skip_tokens: set[str] = set(lex.get("skip", []))
        self.balanced: dict[str, str] = dict(lex.get("balanced") or {})
        self.non_terminals: list[str] = list(parse["non_terminals"])
        self.productions: list[dict] = list(parse["productions"])
        self.start_state: int = parse.get("start_state", 0)
        self.end_marker: str = parse.get("end_marker", "$")

        self.token_index: dict[str, int] = {self.end_marker: 0}
        for i, name in enumerate(self.tokens, start=1):
            self.token_index[name] = i

        self.nt_index: dict[str, int] = {name: i for i, name in enumerate(self.non_terminals)}


def _emit_module(ctx: _LuaCtx) -> str:
    out: list[str] = [GENERATED_NOTE, "local M = {}", ""]

    # Token / NT enums (values match the C / C++ backends so cross-language
    # interop with the same JSON bundle is straightforward).
    out.append("M.TOK = {")
    out.append("    _EndOfInput_ = 0,")
    for name, idx in ((tok, i + 1) for i, tok in enumerate(ctx.tokens)):
        out.append(f"    {_lua_safe_name(name)} = {idx},")
    out.append("}")
    out.append("")
    out.append("M.NT = {")
    aug_start = ctx.bundle["parse"].get("augmented_start", "$start")
    for i, nt in enumerate(ctx.non_terminals):
        key = "_Start_" if nt == aug_start else _lua_safe_name(nt)
        out.append(f"    {key} = {i},")
    out.append("}")
    out.append("")

    out.extend(_emit_lex_tables(ctx))
    out.append("")
    out.extend(_emit_parse_tables(ctx))
    out.append("")
    out.extend(_emit_name_tables(ctx))
    out.append("")
    out.extend(_emit_runtime(ctx))

    plan = ast_emit.plan_from_bundle(ctx.bundle)
    if plan is not None:
        out.extend(ast_emit.emit_ast(plan))

    out.append("")
    out.append("return M")
    return "\n".join(out) + "\n"


def _emit_lex_tables(ctx: _LuaCtx) -> list[str]:
    states = ctx.lex["states"]
    n = len(states)
    # rows[sid][b] holds the raw target state (0..n-1) or -1 for DEAD.
    # The +1 to Lua's 1-based encoding happens only at emit time.
    rows = [[-1] * 256 for _ in range(n)]
    accepts = [-1] * n
    for entry in states:
        sid = entry["id"]
        for byte_str, tgt in entry.get("edges", {}).items():
            rows[sid][int(byte_str, 16)] = tgt
        if entry.get("accept"):
            accepts[sid] = ctx.token_index[entry["accept"]]

    skip_bits = [1 if name in ctx.skip_tokens else 0 for name in ctx.tokens]

    out: list[str] = []
    out.append("local kDfaStateCount = " + str(n))
    out.append("-- DFA transition table: dfa_trans[state+1][byte+1] -> next_state+1, or 0 for DEAD.")
    out.append("local dfa_trans = {")
    for s in range(n):
        # Encode tgt -> tgt + 1 here (1-based), -1 -> 0 sentinel for DEAD.
        cells = ", ".join(str(v + 1 if v >= 0 else 0) for v in rows[s])
        out.append(f"    {{ {cells} }},")
    out.append("}")
    out.append("")
    out.append("-- Accept token-kind for each state, or -1 for non-accepting.")
    out.append("local dfa_accept = {")
    for s in range(n):
        out.append(f"    {accepts[s]},")
    out.append("}")
    out.append("")
    out.append("-- 1 if the token at this kind index is %skip, else 0. Indexed by token kind.")
    out.append("local token_skip = {")
    out.append("    [0] = 0,  -- _EndOfInput_")
    for tok, bit in zip(ctx.tokens, skip_bits):
        out.append(f"    [{ctx.token_index[tok]}] = {bit},  -- {tok}")
    out.append("}")
    out.append("")
    out.append("-- Per-token close-delimiter byte for %balanced= tokens.")
    out.append("-- 0 means the token is not balanced.")
    out.append("local token_balanced = {")
    out.append("    [0] = 0,  -- _EndOfInput_")
    for tok in ctx.tokens:
        close_char = ctx.balanced.get(tok)
        if close_char is None:
            out.append(f"    [{ctx.token_index[tok]}] = 0,  -- {tok}")
        else:
            out.append(f"    [{ctx.token_index[tok]}] = {ord(close_char)},  -- {tok}")
    out.append("}")
    return out


def _emit_parse_tables(ctx: _LuaCtx) -> list[str]:
    n_states = len(ctx.parse["states"])
    n_tokens = len(ctx.tokens) + 1
    n_nts = len(ctx.non_terminals)
    n_prods = len(ctx.productions)

    action = [[0] * n_tokens for _ in range(n_states)]
    goto = [[-1] * n_nts for _ in range(n_states)]

    for entry in ctx.parse["states"]:
        s = entry["id"]
        for term, code in entry["actions"].items():
            tk = ctx.token_index[term]
            if code == "acc":
                action[s][tk] = -32768
            elif code.startswith("s"):
                action[s][tk] = int(code[1:]) + 1
            elif code.startswith("r"):
                action[s][tk] = -(int(code[1:]) + 1)
            else:
                raise ValueError(f"unrecognised action code {code!r}")
        for nt, tgt in entry["gotos"].items():
            goto[s][ctx.nt_index[nt]] = tgt

    out: list[str] = []
    out.append(f"local kParseStateCount = {n_states}")
    out.append(f"local kProductionCount = {n_prods}")
    out.append(f"local kStartState      = {ctx.start_state}")
    out.append(f"local kTokenCount      = {n_tokens}")
    out.append("local kActionAccept    = -32768")
    out.append("")
    out.append("-- ACTION[state+1][token_kind+1]: 0=error, +N=shift to N-1, -K=reduce by K-1, -32768=accept.")
    out.append("local action = {")
    for s in range(n_states):
        out.append("    { " + ", ".join(str(v) for v in action[s]) + " },")
    out.append("}")
    out.append("")
    out.append("-- GOTO[state+1][nt_kind+1]: next-state (0-indexed), or -1 for none.")
    out.append("local goto_tbl = {")
    for s in range(n_states):
        out.append("    { " + ", ".join(str(v) for v in goto[s]) + " },")
    out.append("}")
    out.append("")

    # default_reduction[state+1] = production index (0-indexed) to reduce by,
    # or -1 if the state has no default reduction. See LRTable.default_reductions.
    default_reductions = [-1] * n_states
    for entry in ctx.parse["states"]:
        if "default_reduction" in entry:
            default_reductions[entry["id"]] = entry["default_reduction"]
    out.append("-- default_reduction[state+1]: production to reduce when ACTION misses, or -1.")
    out.append("local default_reduction = {")
    chunk = 16
    for i in range(0, n_states, chunk):
        out.append("    " + ", ".join(str(v) for v in default_reductions[i : i + chunk]) + ",")
    out.append("}")
    out.append("")

    out.append("local productions = {")
    for p in ctx.productions:
        out.append(f"    {{ lhs = {ctx.nt_index[p['lhs']]}, rhs_len = {len(p['rhs'])} }},")
    out.append("}")
    return out


def _emit_name_tables(ctx: _LuaCtx) -> list[str]:
    out: list[str] = []
    out.append("local token_names = {")
    out.append('    [0] = "$",')
    for tok in ctx.tokens:
        out.append(f'    [{ctx.token_index[tok]}] = "{_lua_string_escape(tok)}",')
    out.append("}")
    out.append("")
    out.append("local nt_names = {")
    for i, nt in enumerate(ctx.non_terminals):
        out.append(f'    [{i}] = "{_lua_string_escape(nt)}",')
    out.append("}")
    out.append("")
    out.append("function M.token_name(k) return token_names[k] or '<invalid>' end")
    out.append("function M.nt_name(k)    return nt_names[k]    or '<invalid>' end")
    return out


def _emit_runtime(ctx: _LuaCtx) -> list[str]:
    return [
        "local Parser = {}",
        "Parser.__index = Parser",
        "",
        "function M.new(input)",
        "    local self = setmetatable({}, Parser)",
        "    self.input = input or ''",
        "    self.lex_pos = 1   -- 1-based string index",
        "    self.lex_line = 1",
        "    self.lex_column = 1",
        "    self.error = nil",
        "    self.root = nil",
        "    return self",
        "end",
        "",
        "-- Returns kind, pos, len, line, col on success; nil on lex error.",
        "function Parser:next_token()",
        "    while true do",
        "        if self.lex_pos > #self.input then",
        "            return 0, self.lex_pos, 0, self.lex_line, self.lex_column",
        "        end",
        "        local state = 1  -- 1-based; state 0 is index 1",
        "        local last_accept_end = -1",
        "        local last_accept_kind = -1",
        "        if dfa_accept[1] >= 0 then",
        "            last_accept_end = self.lex_pos",
        "            last_accept_kind = dfa_accept[1]",
        "        end",
        "        local i = self.lex_pos",
        "        while i <= #self.input do",
        "            local b = string.byte(self.input, i)",
        "            local nxt = dfa_trans[state][b + 1]",
        "            if nxt == 0 then break end",
        "            state = nxt",
        "            i = i + 1",
        "            local accept_kind = dfa_accept[state]",
        "            if accept_kind >= 0 then",
        "                last_accept_end = i",
        "                last_accept_kind = accept_kind",
        "            end",
        "        end",
        "        if last_accept_end <= self.lex_pos then",
        "            self.error = string.format('lexical error at line %d, column %d',",
        "                                       self.lex_line, self.lex_column)",
        "            return nil",
        "        end",
        "        -- %balanced= extension: extend the match by counting nested",
        "        -- open/close pairs until depth returns to zero. Open is whatever",
        "        -- the DFA already consumed at self.lex_pos.",
        "        local close_b = token_balanced[last_accept_kind]",
        "        if close_b ~= 0 then",
        "            local open_b = string.byte(self.input, self.lex_pos)",
        "            local depth = 1",
        "            local j = last_accept_end",
        "            while j <= #self.input and depth > 0 do",
        "                local b = string.byte(self.input, j)",
        "                if b == open_b then depth = depth + 1",
        "                elseif b == close_b then depth = depth - 1 end",
        "                j = j + 1",
        "            end",
        "            if depth ~= 0 then",
        "                self.error = string.format('unterminated balanced token at line %d, column %d',",
        "                                           self.lex_line, self.lex_column)",
        "                return nil",
        "            end",
        "            last_accept_end = j",
        "        end",
        "        local start = self.lex_pos",
        "        local sline = self.lex_line",
        "        local scol = self.lex_column",
        "        for p = start, last_accept_end - 1 do",
        "            if string.byte(self.input, p) == 10 then  -- \\n",
        "                self.lex_line = self.lex_line + 1",
        "                self.lex_column = 1",
        "            else",
        "                self.lex_column = self.lex_column + 1",
        "            end",
        "        end",
        "        self.lex_pos = last_accept_end",
        "        if token_skip[last_accept_kind] == 1 then",
        "            -- continue outer loop",
        "        else",
        "            return last_accept_kind, start, last_accept_end - start, sline, scol",
        "        end",
        "    end",
        "end",
        "",
        "function Parser:parse()",
        "    local state_stack = { kStartState }",
        "    local value_stack = {}",
        "",
        "    local function apply_filter(kind, pos, len)",
        "        if self.token_filter then",
        "            local text = string.sub(self.input, pos, pos + len - 1)",
        "            return self.token_filter(self, kind, text, len) or kind",
        "        end",
        "        return kind",
        "    end",
        "",
        "    local la_kind, la_pos, la_len, la_line, la_col = self:next_token()",
        "    if la_kind == nil then return false end",
        "    la_kind = apply_filter(la_kind, la_pos, la_len)",
        "",
    "    while true do",
        "        local s = state_stack[#state_stack]",
        "        local act = action[s + 1][la_kind + 1]",
        "        if act == 0 then",
        "            local dr = default_reduction[s + 1]",
        "            if dr >= 0 then",
        "                -- Default reduction: this state's only valid action is",
        "                -- to reduce by `dr`. Doing it eagerly lets a host-side",
        "                -- token rewriter (e.g. C typedef-name hack) update its",
        "                -- tables before the next ACTION lookup classifies the",
        "                -- pending lookahead. See LRTable.default_reductions.",
        "                act = -(dr + 1)",
        "            else",
        "                -- Build 'unexpected ... ; expected one of: ...' by walking the",
        "                -- action row at runtime. End-of-input is rendered as",
        "                -- '<end of input>' to match the Python runtime's wording.",
        "                local head",
        "                if la_kind == 0 then",
        "                    head = 'unexpected end of input'",
        "                else",
        "                    head = string.format('unexpected token %s at line %d, column %d',",
        "                                          M.token_name(la_kind), la_line, la_col)",
        "                end",
        "                local shown, total, parts = 0, 0, {}",
        "                for t = 0, kTokenCount - 1 do",
        "                    if action[s + 1][t + 1] ~= 0 then",
        "                        total = total + 1",
        "                        if shown < 12 then",
        "                            local nm",
        "                            if t == 0 then nm = '<end of input>' else nm = M.token_name(t) end",
        "                            parts[#parts + 1] = nm",
        "                            shown = shown + 1",
        "                        end",
        "                    end",
        "                end",
        "                local expect = ''",
        "                if shown > 0 then",
        "                    expect = '; expected one of: ' .. table.concat(parts, ', ')",
        "                    if total > shown then",
        "                        expect = expect .. string.format(', ... +%d more', total - shown)",
        "                    end",
        "                end",
        "                self.error = head .. expect",
        "                return false",
        "            end",
        "        end",
        "        if act == kActionAccept then",
        "            self.root = value_stack[#value_stack]",
        "            return true",
        "        end",
        "        if act > 0 then",
        "            local leaf = {",
        "                is_terminal = true,",
        "                kind = la_kind,",
        "                production = -1,",
        "                line = la_line,",
        "                column = la_col,",
        "                text = string.sub(self.input, la_pos, la_pos + la_len - 1),",
        "                children = {},",
        "            }",
        "            state_stack[#state_stack + 1] = act - 1",
        "            value_stack[#value_stack + 1] = leaf",
        "            la_kind, la_pos, la_len, la_line, la_col = self:next_token()",
        "            if la_kind == nil then return false end",
        "            la_kind = apply_filter(la_kind, la_pos, la_len)",
        "        else",
        "            local prod_idx = -act - 1",
        "            local p = productions[prod_idx + 1]",
        "            local rhs_len = p.rhs_len",
        "            local node = {",
        "                is_terminal = false,",
        "                kind = p.lhs,",
        "                production = prod_idx,",
        "                line = 0,",
        "                column = 0,",
        "                text = nil,",
        "                children = {},",
        "            }",
        "            if rhs_len > 0 then",
        "                local base = #value_stack - rhs_len",
        "                for i = 1, rhs_len do",
        "                    node.children[i] = value_stack[base + i]",
        "                end",
        "                if node.children[1] then",
        "                    node.line = node.children[1].line",
        "                    node.column = node.children[1].column",
        "                end",
        "                for _ = 1, rhs_len do",
        "                    state_stack[#state_stack] = nil",
        "                    value_stack[#value_stack] = nil",
        "                end",
        "            end",
        "            local gs = state_stack[#state_stack]",
        "            local target = goto_tbl[gs + 1][p.lhs + 1]",
        "            if target < 0 then",
        "                value_stack[#value_stack + 1] = node",
        "            else",
        "                state_stack[#state_stack + 1] = target",
        "                value_stack[#value_stack + 1] = node",
        "            end",
        "            -- Fire the post-reduce hook BEFORE re-running the token filter,",
        "            -- so a hook that updates host state is visible to the filter on",
        "            -- its next call.",
        "            if self.post_reduce then self.post_reduce(self, prod_idx, node) end",
        "            -- Re-apply the filter so a host-side semantic action that",
        "            -- updated state during this reduce sees its change reflected",
        "            -- in the pending lookahead before the next ACTION lookup.",
        "            la_kind = apply_filter(la_kind, la_pos, la_len)",
        "        end",
        "    end",
        "end",
        "",
        "-- Install a token filter. Receives (parser, kind, text, len) and returns",
        "-- the (possibly rewritten) kind. Lookups happen on every fetched lookahead",
        "-- and after every reduction. Set to nil to disable.",
        "function Parser:set_token_filter(fn)",
        "    self.token_filter = fn",
        "end",
        "",
        "-- Install a post-reduce hook. Receives (parser, prod_idx, node) and is",
        "-- invoked after every successful reduction, before the runtime re-runs",
        "-- the token filter. Pair with set_token_filter to track host state.",
        "function Parser:set_post_reduce(fn)",
        "    self.post_reduce = fn",
        "end",
    ]


def _lua_safe_name(name: str) -> str:
    """Lua-safe identifier suffix. Most token / nt names already work."""
    return re.sub(r"[^A-Za-z0-9_]", "_", name)


def _lua_string_escape(s: str) -> str:
    return s.replace("\\", "\\\\").replace('"', '\\"')
