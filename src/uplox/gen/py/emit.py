"""Python backend: emit a self-contained module that reuses the uplox runtime.

The emitted file is a thin wrapper — it embeds the canonical JSON bundle
as a literal and exposes a top-level :func:`parse` that drives the
:mod:`uplox.parse.runtime` driver. Hosts that ship a uplox-generated parser
to a Python app vendor the emitted module and pull in uplox as a runtime
dependency. The runtime is small (lexer + LR driver + hooks) and shared
across all grammars, so embedding versus duplicating is a clear win.

The module also exposes the embedded ``BUNDLE`` so hosts can inspect the
schema directly (token names, productions, etc.) without re-parsing.

v3 auto-AST: grammars whose bundles carry a non-empty ``ast`` section
emit an *additional* layer — per-kind ``@dataclass`` types, a typed
``AstNode`` union, a per-production action table, and a ``parse()``
that returns an ``AstNode`` rather than a ``ParseNode``. The unchanged
``parse_cst()`` entry remains for consumers that still want the raw
parse tree. Grammars without annotations emit the original
``ParseNode``-returning module unchanged.
"""

from __future__ import annotations

import json
import re
from typing import Any

from ...tables import ast_from_json
from ...spec.ast_plan import AstPlan, NodeField, NodeKind, ProductionAction


_VALID_PREFIX = re.compile(r"^[a-zA-Z_][a-zA-Z0-9_]*$")


def emit_py(bundle: dict[str, Any], *, prefix: str | None = None) -> str:
    """Emit a Python module text for ``bundle``.

    ``prefix`` overrides the bundle's grammar name in the module's TOKENS
    constant and serves as a sanity check that the value is a legal Python
    identifier — emitted names use it directly.
    """
    grammar = (prefix or bundle["meta"]["grammar"]).strip()
    if not _VALID_PREFIX.match(grammar):
        raise ValueError(
            f"prefix {grammar!r} is not a valid Python identifier; "
            "use --prefix to override"
        )

    ast_plan = ast_from_json(bundle.get("ast", {}))
    bundle_json = json.dumps(bundle, indent=2, sort_keys=True)
    # Triple-quote-safe encoding: backslash any embedded triple quote.
    bundle_json_safe = bundle_json.replace('"""', r'\"""')

    if ast_plan is None:
        return _emit_cst_module(grammar, bundle_json_safe)
    return _emit_ast_module(grammar, bundle_json_safe, ast_plan)


# ---- CST-only emission (back-compat) ----------------------------------------


def _emit_cst_module(grammar: str, bundle_json_safe: str) -> str:
    """Pre-v3 emission: grammar has no AST annotations.

    Output is byte-identical to what the v2 emitter produced; consumers
    relying on the existing API path don't see any change.
    """
    lines: list[str] = [
        f'"""Auto-generated Python parser for grammar {grammar!r}.',
        "",
        "Generated by uplox. Depends on the uplox runtime at import time;",
        "no other state is shared with the generator.",
        '"""',
        "",
        "from __future__ import annotations",
        "",
        "import json",
        "from typing import Any, Callable, Iterable",
        "",
        "from uplox.lex.scanner import Scanner, Token",
        "from uplox.parse.runtime import (",
        "    HookRegistry,",
        "    ParseContext,",
        "    ParseError,",
        "    ParseNode,",
        "    SemanticAction,",
        "    StackValue,",
        "    TokenFilter,",
        "    parse as _runtime_parse,",
        ")",
        "from uplox.tables import balanced_from_json, dfa_from_json, table_from_json",
        "",
        "",
        'BUNDLE: dict[str, Any] = json.loads(r"""',
        bundle_json_safe,
        '""")',
        "",
        '_dfa, _tokens, _skip = dfa_from_json(BUNDLE["lex"])',
        '_table = table_from_json(BUNDLE["parse"])',
        "_scanner = Scanner(",
        "    dfa=_dfa,",
        "    skip_tokens=frozenset(_skip),",
        '    balanced=balanced_from_json(BUNDLE["lex"]),',
        ")",
        "",
        "TOKENS: list[str] = list(_tokens)",
        "SKIP: list[str] = list(_skip)",
        f"GRAMMAR_NAME: str = {grammar!r}",
        "",
        "",
        "def parse(",
        "    text: str,",
        "    *,",
        "    hooks: HookRegistry | None = None,",
        "    semantic_actions: dict[int, SemanticAction] | None = None,",
        "    token_filter: TokenFilter | None = None,",
        ") -> StackValue:",
        f'    """Parse ``text`` against the embedded {grammar} grammar.',
        "",
        "    Returns the value associated with the start symbol — by default a",
        "    :class:`uplox.parse.runtime.ParseNode`, but anything a host's",
        "    ``semantic_actions`` returns is fine.",
        "",
        "    Raises :class:`uplox.parse.runtime.ParseError` on a syntax error or",
        "    :class:`uplox.lex.scanner.ScanError` on a lexical error.",
        '    """',
        "    return _runtime_parse(",
        "        _table,",
        "        _scanner.scan(text),",
        "        hooks=hooks or HookRegistry(ignore_missing=True),",
        "        semantic_actions=semantic_actions,",
        "        token_filter=token_filter,",
        "    )",
        "",
        "",
        "def scan(text: str) -> Iterable[Token]:",
        '    """Run only the lexer on ``text`` and yield tokens. Useful for"""',
        '    """tooling that wants the token stream without parsing."""',
        "    return _scanner.scan(text)",
        "",
        "",
        "__all__ = [",
        '    "BUNDLE", "TOKENS", "SKIP", "GRAMMAR_NAME",',
        '    "parse", "scan",',
        '    "HookRegistry", "ParseContext", "ParseError", "ParseNode", "Token",',
        "]",
        "",
    ]
    return "\n".join(lines)


# ---- AST emission (v3) ------------------------------------------------------


def _emit_ast_module(grammar: str, bundle_json_safe: str, plan: AstPlan) -> str:
    """Emit a v3 module with typed AST dataclasses and an AST-returning parse()."""

    lines: list[str] = [
        f'"""Auto-generated Python parser+AST for grammar {grammar!r}.',
        "",
        "Generated by uplox v3. ``parse(text)`` returns an :data:`AstNode`",
        "built directly from the grammar's annotations. ``parse_cst(text)``",
        "is preserved for consumers that need the raw parse tree.",
        '"""',
        "",
        "from __future__ import annotations",
        "",
        "import json",
        "from dataclasses import dataclass, field",
        "from typing import Any, Callable, Iterable, Optional, Union",
        "",
        "from uplox.lex.scanner import Scanner, Token",
        "from uplox.parse.runtime import (",
        "    HookRegistry,",
        "    ParseContext,",
        "    ParseError,",
        "    ParseNode,",
        "    StackValue,",
        "    TokenFilter,",
        "    parse as _runtime_parse,",
        ")",
        "from uplox.tables import balanced_from_json, dfa_from_json, table_from_json",
        "",
        "",
        'BUNDLE: dict[str, Any] = json.loads(r"""',
        bundle_json_safe,
        '""")',
        "",
        '_dfa, _tokens, _skip = dfa_from_json(BUNDLE["lex"])',
        '_table = table_from_json(BUNDLE["parse"])',
        "_scanner = Scanner(",
        "    dfa=_dfa,",
        "    skip_tokens=frozenset(_skip),",
        '    balanced=balanced_from_json(BUNDLE["lex"]),',
        ")",
        "",
        "TOKENS: list[str] = list(_tokens)",
        "SKIP: list[str] = list(_skip)",
        f"GRAMMAR_NAME: str = {grammar!r}",
        "",
        "",
    ]

    # ---- _Pos and dataclasses ----------------------------------------------
    lines.extend(_emit_pos_class())
    lines.append("")
    for kind in plan.node_kinds:
        lines.extend(_emit_dataclass(kind))
        lines.append("")

    # ---- AstNode union -----------------------------------------------------
    kind_names = [k.name for k in plan.node_kinds]
    if len(kind_names) == 1:
        lines.append(f"AstNode = {kind_names[0]}")
    else:
        lines.append("AstNode = Union[")
        for n in kind_names:
            lines.append(f"    {n},")
        lines.append("]")
    lines.append("")
    lines.append("")

    # ---- Position helper ---------------------------------------------------
    lines.extend(_emit_pos_helper())
    lines.append("")

    # ---- Per-production actions --------------------------------------------
    # User productions are 0-indexed in the IR; grammar productions are
    # 1-indexed (production 0 is the augmented start). Map user_index ->
    # grammar_index here when building the action table.
    lines.extend(_emit_action_table(plan))
    lines.append("")

    # ---- parse() and parse_cst() -------------------------------------------
    lines.extend(_emit_parse_functions(grammar))
    lines.append("")

    # ---- __all__ -----------------------------------------------------------
    exported = ["BUNDLE", "TOKENS", "SKIP", "GRAMMAR_NAME", "parse", "parse_cst", "scan",
                "AstNode", "_Pos",
                "HookRegistry", "ParseContext", "ParseError", "ParseNode", "Token"]
    exported.extend(kind_names)
    lines.append("__all__ = [")
    for n in exported:
        lines.append(f"    {n!r},")
    lines.append("]")
    lines.append("")

    return "\n".join(lines)


def _emit_pos_class() -> list[str]:
    return [
        "@dataclass",
        "class _Pos:",
        '    """Source-position span carried on every AST node."""',
        "    start_line: int = 0",
        "    start_column: int = 0",
        "    end_line: int = 0",
        "    end_column: int = 0",
    ]


def _emit_dataclass(kind: NodeKind) -> list[str]:
    """Emit one ``@dataclass(kw_only=True)`` for an AST node kind.

    Keyword-only sidesteps the "non-default before default" ordering
    constraint — every field can carry a default if it has one (list,
    optional) without forcing the caller to remember field order.
    """
    out = ["@dataclass(kw_only=True)", f"class {kind.name}:"]
    if not kind.fields:
        # A node with no fields is unusual but valid (e.g., a marker
        # like NilLiteral). Emit a `pass` to keep the class non-empty.
        out.append('    """AST node kind."""')
        out.append("    pos: _Pos = field(default_factory=_Pos)")
        return out
    out.append(f'    """AST node {kind.name!r}."""')
    for f in kind.fields:
        out.append(f"    {_field_decl(f)}")
    out.append("    pos: _Pos = field(default_factory=_Pos)")
    return out


def _field_decl(f: NodeField) -> str:
    """Format one ``name: type [= default]`` line."""
    if f.type_kind == "token":
        if f.optional:
            return f"{f.name}: Optional[Token] = None"
        return f"{f.name}: Token"
    if f.type_kind == "list":
        # element_kind names a non-terminal whose union of kinds is the
        # element type. We type as list[AstNode] to keep things simple
        # — refining to a per-list-kind union is a v3.1 polish.
        return f"{f.name}: list[AstNode] = field(default_factory=list)"
    # type_kind == "node"
    if f.optional:
        return f"{f.name}: Optional[AstNode] = None"
    return f"{f.name}: AstNode"


def _emit_pos_helper() -> list[str]:
    return [
        "def _pos_from_rhs(rhs: list) -> _Pos:",
        '    """Compute a _Pos span from the leftmost/rightmost positioned RHS values."""',
        "    start_line = start_col = 0",
        "    end_line = end_col = 0",
        "    for v in rhs:",
        "        if v is None:",
        "            continue",
        "        if isinstance(v, Token):",
        "            start_line, start_col = v.line, v.column",
        "            break",
        "        pos = getattr(v, 'pos', None)",
        "        if pos is not None:",
        "            start_line, start_col = pos.start_line, pos.start_column",
        "            break",
        "    for v in reversed(rhs):",
        "        if v is None:",
        "            continue",
        "        if isinstance(v, Token):",
        "            end_line, end_col = v.line, v.column + len(v.text)",
        "            break",
        "        pos = getattr(v, 'pos', None)",
        "        if pos is not None:",
        "            end_line, end_col = pos.end_line, pos.end_column",
        "            break",
        "    return _Pos(start_line, start_col, end_line, end_col)",
    ]


def _emit_action_table(plan: AstPlan) -> list[str]:
    """Emit ``_AST_ACTIONS`` as a dict keyed by grammar production index.

    Each entry is a lambda computing the new stack value from
    ``(ctx, rhs)``. The runtime calls it during reduction.
    """
    # Field-type lookup: (kind_name, field_name) -> NodeField.type_kind.
    # Used by _action_body to wrap list-field assignments with `or []`,
    # so an absent _unwrap wrapper (empty alt) yields the schema's
    # default empty list instead of None on the parent.
    field_types: dict[tuple[str, str], str] = {}
    for kind in plan.node_kinds:
        for f in kind.fields:
            field_types[(kind.name, f.name)] = f.type_kind

    out = ["_AST_ACTIONS: dict[int, Callable[[ParseContext, list], Any]] = {"]
    # The augmented-start production (grammar index 0) lifts rhs[0] —
    # that's the user's start non-terminal's value, which by the time
    # production 0 reduces is already an AstNode.
    out.append("    0: lambda ctx, rhs: rhs[0],")
    for action in plan.production_actions:
        grammar_idx = action.user_index + 1
        body = _action_body(action, field_types)
        out.append(f"    {grammar_idx}: {body},")
    out.append("}")
    return out


def _action_body(
    action: ProductionAction,
    field_types: dict[tuple[str, str], str] | None = None,
) -> str:
    """Build the lambda body for one ProductionAction.

    ``field_types`` maps ``(kind_name, field_name)`` to the field's
    ``type_kind`` (one of ``"node"`` / ``"token"`` / ``"list"``). When
    a named-kind action assigns a list field from an RHS position, the
    rhs slot may carry ``None`` because the source non-terminal's
    empty alt fired (via ``_unwrap`` look-through). Coalesce to ``[]``
    per the OQ1 resolution: list fields default to empty list, never
    None.
    """
    if field_types is None:
        field_types = {}
    kind = action.kind
    if kind == "_lift":
        if action.source_index < 0:
            # Sentinel for empty alts: produce None so the parent's
            # optional/list logic does the right thing.
            return "lambda ctx, rhs: None"
        return f"lambda ctx, rhs: rhs[{action.source_index}]"
    if kind == "_unwrap":
        # Same runtime behaviour as _lift; the spec-level distinction
        # is captured in the schema, not in the action body.
        return f"lambda ctx, rhs: rhs[{action.source_index}]"
    if kind == "_list_init":
        # element_indices may have one entry (single-element init) or
        # several (e.g. cowgol's <multi_target> requires at-least-two).
        items = ", ".join(f"rhs[{i}]" for i in action.element_indices)
        return f"lambda ctx, rhs: [{items}]"
    if kind == "_list_extend":
        elem_idx = action.element_indices[0]
        # Order matters: in a left-recursive list (<self> ... <element>)
        # the accumulator comes first and we append; in a right-recursive
        # list (<element> <self>) the element comes first and we prepend
        # so the resulting list preserves source order.
        if elem_idx < action.accumulator_index:
            return (
                f"lambda ctx, rhs: [rhs[{elem_idx}]] + "
                f"rhs[{action.accumulator_index}]"
            )
        return (
            f"lambda ctx, rhs: rhs[{action.accumulator_index}] + "
            f"[rhs[{elem_idx}]]"
        )
    if kind == "_list_empty":
        return "lambda ctx, rhs: []"
    # Named kind construction. List fields wrap with `or []` so an
    # empty-alt wrapper (None value) becomes the empty list default.
    parts: list[str] = []
    for fa in action.field_assignments:
        ft = field_types.get((kind, fa.field_name))
        if ft == "list":
            parts.append(f"{fa.field_name}=(rhs[{fa.rhs_index}] or [])")
        else:
            parts.append(f"{fa.field_name}=rhs[{fa.rhs_index}]")
    field_args = ", ".join(parts)
    if field_args:
        return f"lambda ctx, rhs: {kind}({field_args}, pos=_pos_from_rhs(rhs))"
    return f"lambda ctx, rhs: {kind}(pos=_pos_from_rhs(rhs))"


def _emit_parse_functions(grammar: str) -> list[str]:
    return [
        "def parse(",
        "    text: str,",
        "    *,",
        "    hooks: HookRegistry | None = None,",
        "    token_filter: TokenFilter | None = None,",
        ") -> AstNode:",
        f'    """Parse ``text`` against the embedded {grammar} grammar.',
        "",
        "    Returns the typed AST root produced by the grammar's",
        "    annotations. Raises :class:`uplox.parse.runtime.ParseError`",
        "    on a syntax error.",
        "",
        "    Hosts that want the raw parse tree should call",
        "    :func:`parse_cst` instead.",
        '    """',
        "    return _runtime_parse(",
        "        _table,",
        "        _scanner.scan(text),",
        "        hooks=hooks or HookRegistry(ignore_missing=True),",
        "        semantic_actions=_AST_ACTIONS,",
        "        token_filter=token_filter,",
        "    )",
        "",
        "",
        "def parse_cst(",
        "    text: str,",
        "    *,",
        "    hooks: HookRegistry | None = None,",
        "    token_filter: TokenFilter | None = None,",
        ") -> StackValue:",
        f'    """Parse ``text`` and return the raw parse tree (CST).',
        "",
        "    Useful for editor integrations or debug viewers that want",
        "    the concrete-syntax shape rather than the AST.",
        '    """',
        "    return _runtime_parse(",
        "        _table,",
        "        _scanner.scan(text),",
        "        hooks=hooks or HookRegistry(ignore_missing=True),",
        "        token_filter=token_filter,",
        "    )",
        "",
        "",
        "def scan(text: str) -> Iterable[Token]:",
        '    """Run only the lexer on ``text`` and yield tokens."""',
        "    return _scanner.scan(text)",
    ]
